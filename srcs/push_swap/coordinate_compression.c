#include "push_swap.h"

static int	ft_strcmp(char *s1, char *s2)
{
	while (*s1 == *s2)
	{
		if (*s1 == '\0')
			return (0);
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}

void	coordinate_compression(char **pivot, int size)
{
	int		i;
	int		j;
	char	*temp;

	i = 0;
	while (i < size - 1)
	{
		j = 0;
		while (j < size - 1 - i)
		{
			if (ft_strcmp(pivot[j], pivot[j + 1]) > 0)
			{
				temp = pivot[j];
				pivot[j] = pivot[j + 1];
				pivot[j + 1] = temp;
			}
			j++;
		}
		i++;
	}
}

